SELECT Q.ID,Q.YEAR,ISNULL(Y.NPV,0) AS[NPV]
FROM QUERIES_TABLE AS Q
LEFT OUTER JOIN
YEAR_TABLE AS Y
ON 
Q.ID = Y.ID
AND
Y.YEAR = Q.YEAR